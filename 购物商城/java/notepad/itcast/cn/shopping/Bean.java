package notepad.itcast.cn.shopping;

public class Bean {
    private String id;
    private String name;
    private String price;

    public void setId(String id){
        this.id=id;
    }
    public String getId(){
        return id;
    }
    public String getName(){
        return name;
    }
    public void setName(){
        this.name=name;
    }
    public String getPrice(){
        return price;
    }
    public void setPrice(){
        this.price=price;
    }
}
